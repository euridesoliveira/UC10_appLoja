    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int linha = tableCidades.getSelectedRow();
        if (linha < 0) {
            JOptionPane.showMessageDialog(this, " Selecione uma Cidade ");

        } else {
            int codigo = (int) tableCidades.getValueAt(linha, 0);
            String nome = (String) tableCidades.getValueAt(linha, 1);
            
           int resposta = JOptionPane.showConfirmDialog(this, " Confirme a exclusao da cidade " + nome + "?",
                    "Excluir cidade", JOptionPane.YES_NO_OPTION);
            

            if (resposta == JOptionPane.YES_OPTION) {
                Cidade cidade = new Cidade();
                cidade.setCodigo(codigo);
                CidadeDAO.excluir(cidade);
                carregarTabela();
            }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int linha = tableCidades.getSelectedRow();
        if (linha < 0 ){
            JOptionPane.showMessageDialog(this, "Vc deve selecionae uma cidade");
            
        }else{
            int codigo = (int) tableCidades.getValueAt(linha, 0);
            FrmCidade tela = new FrmCidade(codigo);
            jdpTelaInicial.add(tela);
            tela.setVisible(true);
        }
    }//GEN-LAST:event_btnEditarActionPerformed


